--LAG FUNCTION --
SELECT *,
LAG(DAILY_SALES,1)OVER(PARTITION BY PRODUCT_ID ORDER BY SALE_DATE) AS PREVIOUS_DAY_SALES_VALUE,
ROUND((PREVIOUS_DAY_SALES_VALUE - DAILY_SALES)/DAILY_SALES *100,2) AS PREVIOUS_DAY_SALES_VALUE_PERCENT,
LAG(DAILY_SALES,2)OVER(PARTITION BY PRODUCT_ID ORDER BY SALE_DATE) AS PREVIOUS_TO_PREVIOUS_DAY_SALES_VALUE,
ROUND((PREVIOUS_TO_PREVIOUS_DAY_SALES_VALUE - PREVIOUS_DAY_SALES_VALUE)/PREVIOUS_DAY_SALES_VALUE *100,2) AS PREVIOUS_TO_PREVIOUS_DAY_SALES_VALUE_PERCENT,
LAG(DAILY_SALES,3)OVER(PARTITION BY PRODUCT_ID ORDER BY SALE_DATE) AS BEFORE_TWO_DAYS_SALES,
ROUND((BEFORE_TWO_DAYS_SALES - PREVIOUS_TO_PREVIOUS_DAY_SALES_VALUE)/PREVIOUS_TO_PREVIOUS_DAY_SALES_VALUE *100,2) AS  BEFORE_TWO_DAYS_SALES_PERCENT
FROM NT_SALES;